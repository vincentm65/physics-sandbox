/// Maximum horizontal velocity generated by hydrostatic head. This doubles as
/// a coarse viscosity model: runny liquids turn pressure into faster jets while
/// gels and molten rock dissipate it locally.
fn pressure_speed_of(m: Material) -> i8 {
    match m {
        Water | LiquidNitrogen | Mercury => 4,
        Acid => 3,
        Oil => 2,
        Napalm | Lava => 1,
        _ => 0,
    }
}

fn chunks_x(width: usize) -> usize {
    width.div_ceil(CHUNK_W)
}

fn chunks_y(height: usize) -> usize {
    height.div_ceil(CHUNK_H)
}

fn chunks_len(width: usize, height: usize) -> usize {
    chunks_x(width) * chunks_y(height)
}

fn activate_chunk_neighborhood(
    width: usize,
    height: usize,
    chunks_x: usize,
    x: usize,
    y: usize,
    chunks: &mut [bool],
) {
    if width == 0 || height == 0 || chunks_x == 0 || chunks.is_empty() {
        return;
    }

    let x0 = x.saturating_sub(1) / CHUNK_W;
    let y0 = y.saturating_sub(1) / CHUNK_H;
    let x1 = (x + 1).min(width - 1) / CHUNK_W;
    let y1 = (y + 1).min(height - 1) / CHUNK_H;

    for cy in y0..=y1 {
        for cx in x0..=x1 {
            if let Some(chunk) = chunks.get_mut(cy * chunks_x + cx) {
                *chunk = true;
            }
        }
    }
}

impl World {
    pub(crate) fn rand_life(&mut self, m: Material) -> u16 {
        match m {
            Fire => self.rand_range(FIRE_LIFE_MIN, FIRE_LIFE_MAX),
            Ember => self.rand_range(EMBER_LIFE_MIN, EMBER_LIFE_MAX),
            Steam => self.rand_range(STEAM_LIFE_MIN, STEAM_LIFE_MAX),
            Smoke => self.rand_range(SMOKE_LIFE_MIN, SMOKE_LIFE_MAX),
            _ => 0,
        }
    }

    pub(crate) fn rand_range(&mut self, min: u16, max: u16) -> u16 {
        self.prng.gen_range(min, max)
    }

    /// Generate a random life via `rand_range` then call `put`.  Splitting the
    /// borrow across two statements avoids E0499 nested-mutable-borrow errors.
    pub(crate) fn put_rand_range(&mut self, i: usize, m: Material, min: u16, max: u16) {
        let life = self.rand_range(min, max);
        self.put(i, m, life);
    }
}
