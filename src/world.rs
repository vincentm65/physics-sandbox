/// Instantaneous blast parameters. Duration comes from lingering atmospheric
/// overpressure rather than a multi-tick scripted wave.
#[derive(Clone, Copy)]
pub(crate) struct BlastProfile {
    pub radius: i32,
    /// Peak air-mass injection scale (ambient units at the epicentre).
    pub pressure: i32,
    /// Peak heat pulse in °C at the epicentre.
    pub heat: i16,
    /// Peak structural damage at the epicentre.
    pub damage: u8,
    /// Peak outward impulse in quarter-cell velocity units.
    pub impulse: i8,
}

